package com.example.ECommerce.Entity;

public class Products {

	private int prodId;
	private String inOrOutProduct;
	private String consumerCat;
	private String productCat;
	private String gender;
	private String sampleCost;
	private String remarks;
	private String physicalRefAvail;
	private String productImgUrl;

	
	public int getProdId() {
		return prodId;
	}
	public void setProdId(int prodId) {
		this.prodId = prodId;
	}
	public String getInOrOutProduct() {
		return inOrOutProduct;
	}
	public void setInOrOutProduct(String inOrOutProduct) {
		this.inOrOutProduct = inOrOutProduct;
	}
	public String getConsumerCat() {
		return consumerCat;
	}
	public void setConsumerCat(String consumerCat) {
		this.consumerCat = consumerCat;
	}
	public String getProductCat() {
		return productCat;
	}
	public void setProductCat(String productCat) {
		this.productCat = productCat;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getSampleCost() {
		return sampleCost;
	}
	public void setSampleCost(String sampleCost) {
		this.sampleCost = sampleCost;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getPhysicalRefAvail() {
		return physicalRefAvail;
	}
	public void setPhysicalRefAvail(String physicalRefAvail) {
		this.physicalRefAvail = physicalRefAvail;
	}
	public String getProductImgUrl() {
		return productImgUrl;
	}
	public void setProductImgUrl(String productImgUrl) {
		this.productImgUrl = productImgUrl;
	}
	
	
}
